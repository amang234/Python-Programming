# Given a dictionary in Python, write a Python program to find the
# sum of all items in the dictionary.
# Input : {'a':100, 'b':200, 'c':300}
# Output : 600

Dict = {'a':100, 'b':200, 'c':300}
print(Dict.values())
print("Output:",sum(Dict.values()))

